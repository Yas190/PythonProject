class Apolice:
    def __init__(self, numero, valor):
        self.numero = numero
        self.valor = valor

    def get_numero(self):
        return self.numero

    def get_valor(self):
        return self.valor

    def set_numero(self, numero):
        self.numero = numero

    def set_valor(self, valor):
        self.valor = valor
